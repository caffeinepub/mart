export function slugify(name: string): string {
  return name
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/(^-|-$)/g, "");
}

export function getProductBySlug(slug: string): SampleProduct | undefined {
  return SAMPLE_PRODUCTS.find((p) => slugify(p.name) === slug);
}
